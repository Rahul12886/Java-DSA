import java.util.*;
public class Substring {
    public static void main(String args[]) {
        String s = "Rahul";
        System.out.println(s.substring(0));
        
        String str = "Kumar";
        System.out.println(str.substring(2));
        
        String n = "RahulKumar";
        System.out.println(n.substring(2,6));
    }
}


/*
  -----  OUTPUT  -----
  Rahul
  mar
  hulK

*/
